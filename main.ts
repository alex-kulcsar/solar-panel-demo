function extMoveSun (theSun: Sprite) {
    ticks = (game.runtime() - runtimeStart) / 100
    sunX = 160 - 2 * ticks / 3
    sunY = 7 * (sunX * sunX) / 640 - 7 * sunX / 4 + 70
    theSun.setPosition(sunX, sunY)
}
